function Student(studentId,studentName,birth,email,classRoom,tel,sex,address,note) {
    this.studentId = studentId;
    this.studentName = studentName;
    this.email = email;
    this.birth = birth;
    this.classRoom = classRoom;
    this.tel = tel;
    this.sex = sex;
    this.address = address;
    this.status = 1
    this.note = note
}